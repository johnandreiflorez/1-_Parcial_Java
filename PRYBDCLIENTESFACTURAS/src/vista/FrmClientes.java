    private void listar(){
        String[][] mat;
        Cliente objCliente= new Cliente();
        ControlCliente objControlCliente= new ControlCliente(objCliente);
        mat=objControlCliente.listar();
        int n=mat.length;
        Object objMat[][]= new String[n][3];
        String vecTit[]= new String[3];
        for(int i=0;i<n;i++){
            objMat[i][0]=mat[i][0];
            objMat[i][1]=mat[i][1];
            objMat[i][2]=mat[i][2];
        }
        DefaultTableModel objModelo= new DefaultTableModel(objMat,vecTit);
        tblClientes.setModel(objModelo);
    }
