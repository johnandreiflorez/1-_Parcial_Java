    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String cd=txtCodigo.getText();
        String cm=txtNombre.getText();
        double cr=Double.valueOf(txtCredito.getText());
        Cliente objCliente= new Cliente(cd,cm,cr);
        ControlCliente objControlCliente= new ControlCliente(objCliente);
        objControlCliente.guardar();
        this.listar();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        String cd=txtCodigo.getText();
        Cliente objCliente= new Cliente(cd,"",0);
        ControlCliente objControlCliente= new ControlCliente(objCliente);
        objCliente=objControlCliente.consultar();
        txtNombre.setText(objCliente.getNombre());
        txtCredito.setText(Double.toString(objCliente.getCredito()));
        this.listar();
    }//GEN-LAST:event_btnConsultarActionPerformed

